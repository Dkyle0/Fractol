#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	char	*str;

	i = 0;
	str = s;
	if (s != NULL && f != NULL)
	{
		while (str[i])
		{
			f(&str[i]);
			i++;
		}
	}
}
