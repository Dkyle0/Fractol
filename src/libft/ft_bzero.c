#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*m;

	i = 0;
	m = (unsigned char*)s;
	while (i < n)
	{
		m[i] = '\0';
		i++;
	}
}
