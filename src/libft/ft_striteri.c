#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		i;
	char	*str;

	i = 0;
	str = s;
	if (s != NULL && f != NULL)
	{
		while (str[i])
		{
			f(i, &str[i]);
			i++;
		}
	}
}
