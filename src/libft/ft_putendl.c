#include "libft.h"

void	ft_putendl(char const *s)
{
	int n;

	n = 0;
	if (s != NULL)
	{
		while (s[n] != '\0')
		{
			ft_putchar(s[n]);
			n++;
		}
		ft_putchar('\n');
	}
}
